package com.wjdgus262.ocrtestapplication;

public class Path_Popup_item {

    String name;
    String info;
    String id;

    public Path_Popup_item(String name, String info, String id) {
        this.name = name;
        this.info = info;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
